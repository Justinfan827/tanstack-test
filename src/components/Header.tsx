import { Link } from '@tanstack/react-router'
import {
  ChevronDown,
  ChevronRight,
  Home,
  Menu,
  Network,
  StickyNote,
  X,
} from 'lucide-react'
import { useState } from 'react'
import { LogoText } from '@/components/logo'

